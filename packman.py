def square(x, y):
    "Draw square using path at (x, y)."
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    "Return offset of point in tiles."
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index

def valid(point):
    "Return True if point is valid in tiles."
    index = offset(point)

    if tiles[index] == 0:
        return False

    index = offset(point + 19)

    if tiles[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0

def world():
    "Draw world using path."
    bgcolor('black')
    path.color('blue')

    for index in range(len(tiles)):
        tile = tiles[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')

def move_towards_pacman(ghost):
    """Hace que el fantasma se mueva en direcci칩n a Pac-Man."""
    ghost_pos, ghost_dir = ghost

    # Posibles direcciones
    directions = [
        vector(speed, 0),  # Derecha
        vector(-speed, 0),  # Izquierda
        vector(0, speed),  # Arriba
        vector(0, -speed)  # Abajo
    ]

    # Calcular la mejor direcci칩n hacia Pac-Man
    best_direction = ghost_dir
    min_distance = float('inf')

    for direction in directions:
        new_position = ghost_pos + direction
        if valid(new_position):  # Solo considerar movimientos v치lidos
            distance = abs(pacman.x - new_position.x) + abs(pacman.y - new_position.y)
            if distance < min_distance:
                min_distance = distance
                best_direction = direction

    ghost_dir.x = best_direction.x
    ghost_dir.y = best_direction.y

def move():
    """Mueve a Pac-Man y a los fantasmas."""
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacman + aim):
        pacman.move(aim)

    index = offset(pacman)

    if tiles[index] == 1:
        tiles[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacman.x + 10, pacman.y + 10)
    dot(20, 'yellow')

    for ghost in ghosts:
        move_towards_pacman(ghost)  # Llama a la nueva funci칩n para que los fantasmas se mueven inteligentemente
        ghost[0].move(ghost[1])

        up()
        goto(ghost[0].x + 10, ghost[0].y + 10)
        dot(20, 'red')

    update()

    for ghost in ghosts:
        if abs(pacman - ghost[0]) < 20:
            return

    ontimer(move, 100)

